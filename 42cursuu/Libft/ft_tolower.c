#include <stdio.h>

int	ft_tolower(int c)
{
	if (c > 64 && c < 91)
	{
		c += 32;
	}
	return (c);
}

int	main(void)
{
	char	c;

	c = 'A';
	printf("%c", ft_tolower(c));
	return (0);
}
